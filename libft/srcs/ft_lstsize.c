#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*a;
	int		i;

	if (lst)
	{
		a = lst;
		i = 0;
		while (a != NULL)
		{
			a = a->next;
			i++;
		}
		return (i);
	}
	return (0);
}
